# 特殊字符  转义
# \n \t \\

print('hello\nworld')
print('hello\tworld')
print('hello\\world')

print('it is a desk')
print('it\'s a desk')
print("it is a \"desk\"")
print(r"it is a \"desk\"")  # 始字符串前缀 r, 直接输出全部字面量：it is a \"desk\"
print("it's a 'desk'")
print('it is a "desk"')
