"""
对接下来的3个问题，假定bags包含列表['a', 'b', 'c', 'd']
1) bags[int('3'*2)//11]求值为多少？
2) bags[-1]求值为多少？
3) bags[-2]求值为多少？
"""

bags = ['a', 'b', 'c', 'd']

# 1) bags[int('3'*2)//11]求值为多少？
"""
对字符串'3'作 *2 的连续拼接，结果为 '33'
int('33')转换为整型 33
对11取整，即 33 // 11 = 3
所以bags[int('3'*2)//11] = bags[3]
"""
print(bags[3])  # 求值为 d

# 2) bags[-1]求值为多少？
"""
索引为 -1 为从右边开始计数，表示倒数第一个下标索引
"""
print(bags[-1])  # 求值为 d

# 3) bags[-2]求值为多少？
"""
索引为 -2 为从右边开始计数，表示倒数第二个下标索引
"""
print(bags[-2])  # 求值为 c
