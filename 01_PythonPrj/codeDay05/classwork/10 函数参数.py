# 需求1：求1+1和的函数
# 定义
def sum1():
    result = 1 + 1
    print(result)


# 调用
sum1()


# 函数参数
# 需求2： 求2个数的和
# 定义
def sum2(a, b):  # a,b 定义时写的参数叫做形式参数：形参
    result = a + b
    print(result)


# 调用
sum2(1, 1)  # 调用时传入的参数叫做实际参数：实参
sum2(1, 2)
sum2(2, 3)
sum2(100, 100)

# 作用：
# 通用性更强

# 概念：
# 定义时写的参数叫做形式参数：形参
# 调用时传入的参数叫做实际参数：实参
